package com.andypemberton.jboss.portal.theme.renderer;

public final class RendererConstants {

	private RendererConstants() {
	}

	/**
	 * The css class name applied to region divs
	 */
	public static String REGION_CSS_CLASS = "region";

	/**
	 * The css class name applied to empty region divs
	 */
	public static String EMPTY_REGION_CSS_CLASS = "empty-region";

	/**
	 * The css class name applied to window divs
	 */
	public static String WINDOW_CSS_CLASS = "portlet-window";

	/**
	 * The css class name applied to portlet divs
	 */
	public static String PORTLET_CSS_CLASS = "portlet-content";

	/**
	 * The css class name applied to decoration divs
	 */
	public static String DECORATION_CSS_CLASS = "decoration";

	/**
	 * The css class name applied to title h2s
	 */
	public static String TITLE_CSS_CLASS = "title";

	/**
	 * The css class name applied to controls uls
	 */
	public static String CONTROLS_CSS_CLASS = "controls";

	/**
	 * The value to prefix the window ID for creating portlet skiplinks
	 */
	public static String SKIPLINK_CSS_CLASS = "skip";

	/**
	 * Property key used for hiding specific renderer features
	 */
	public static String PROP_HIDE = "hide";

	/**
	 * Key appended to properties when used to renderer features in specific
	 * scenarios
	 */
	public static String PROP_EMPTY = "empty";

	/**
	 * The default name to search for a properties file with i18n messages for
	 * use in renderers
	 */
	public static String DEFAULT_MESSAGES_NAME = "messages";

	/**
	 * The property to search for a user-specified set i18n resource bundle,
	 * refers to a ResourceBundle baseName
	 */
	public static String PROP_MESSAGES_BASENAME = "renderer.messages.baseName";

	/**
	 * The name of the rendererContext attribute to store the current window's
	 * ID in a WindowRenderer for access in DecorationRenderer and
	 * PortletRenderers
	 */
	public static String ATTR_WINDOW_ID = "currentWindowId";

}
